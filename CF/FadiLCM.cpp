ll gcd(ll a, ll b) {
	if (b == 0)
		return a;
	else
		return gcd(b, a % b);
}
ll lcm(ll a, ll b) {
	return a / gcd(a, b) * b;
}
void solve()
{
	ll n;
	cin >> n;
	ll ans;
	for (ll i = 1 ; i * i <= n ; i++) {
		if (n % i == 0 && lcm(i, n / i) == n) {
			ans = i;
		}
	}
	cout << ans << " " << n / ans;
}


